// escape sequences

//to start new line use "\n"
let message = 'Hello I am a boy, and I am not a girl.\nhow are you';
console.log(message);
// to display ' use backslash i.e "\"
let messageA = 'Hello I\'m a boy, and I\'m not a girl.\nhow are you';
console.log(messageA);
// to diplay \ use blackslash two times i.e "\\"
let messageB = 'Hello \\  I am a boy, and I am not a girl.\nhow are you';
console.log(messageB);
// to display tab space use "\t"
let messageC = '\tHello I am a boy, and I am not a girl.\nhow are you';
console.log(messageC);