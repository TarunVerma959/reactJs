// Math object

//displays PI value
console.log('Pi value = '+Math.PI);
//displays random value each time the page is refreshed
console.log('Random value = '+Math.random());
//displays rounded value for the given value
console.log('Round value = '+Math.round(5.8));
//displays rounded up value to its nearest integer for the given value
console.log('Ceil value = '+Math.ceil(5.8));
//displays rounded doen value to its nearest integer for the given value
console.log('Floor value = '+Math.floor(5.8));
//displays integer part of the given value
console.log('Trunc value = '+Math.trunc(5.8));
//displays square root of the given value
console.log('Square root value = '+Math.sqrt(81));