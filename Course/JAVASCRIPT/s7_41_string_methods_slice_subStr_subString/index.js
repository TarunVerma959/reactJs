// String Methods: slice, subStr, subString

let message = 'Hello I am a boy, and I am not a girl';
// prints message
console.log(message);

//slice method - it slices string between certaing index 
console.log(message.slice(1,9 ));
// it slices the string upto defined index value
console.log(message.slice(2));

// subString method - it returns string between certaing index 
console.log(message.substring(0,3));

// subString method - it returns string between certaing index 
console.log(message.substring(0,3));

// substr method
console.log(message.substr(0,4));