// for loop

// increment case
//   let index = 0; index < array.length; index++       here loop runs 4 times
for (let index = 1; index <= 4; index++) {
    console.log('hello', index);
}

// deccrement case
//   let index = 0; index > array.length; index--       here loop runs 4 times
for (let index = 4; index >= 1; index--) {
    console.log('hello', index);
}