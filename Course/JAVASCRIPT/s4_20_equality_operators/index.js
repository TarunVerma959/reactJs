//Logical operators 
// equality     ===
// notequality  !==


let number1 = 10;

//in tripple equalto it also check for data type of both variable
// it displays for value is equal
console.log(number1 === 10);
console.log(number1 === '10');
// it displays for value is not equal
console.log(number1 !== 10);


//in double equalto it doesnot check for data type of both variable
// it displays for value is equal
console.log(number1 == 10);
console.log(number1 == '10');
// it displays for value is not equal
console.log(number1 != 10);



