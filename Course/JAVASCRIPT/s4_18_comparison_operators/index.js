//comparison operators 
// greater than         > 
// lesser than          <
// greater than equal   >=
// lesser than equal    <=
// equality             ===
// not equal            !==

let number1 = 10;
// it displays true or false
console.log(number1 > 20);
console.log(number1 < 20);
console.log(number1 >= 10);
console.log(number1 <= 5);
console.log(number1 === 10);
console.log(number1 !== 10);
