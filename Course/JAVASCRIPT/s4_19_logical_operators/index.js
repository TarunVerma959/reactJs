//Logical operators 
// and     &&
// or      ||
// not     !

let number1 = 10;

// it displays for true && true
console.log(number1 > 5 && number1 < 15);

// it displays for either 1 is true
console.log(number1 > 5 || number1 < 4);

// it displays for not greater than
console.log(!(number1 > 20));
// it displays for not lesser than
console.log(!(number1 < 20));